#Controlando o incremento da variavel contadora

#escrevendo range(1,10,2), a var assumirá o valor inicial como sendo 1, o valor final como sendo 9 e cada
#volta ela somará 2. Portando terá os valores: 1,3,5,7 e 9.

#a variável contadora deverá assumir valores no intervalo entre 1 e 9 com incremento 2
for x in range(1,10,2):
    #para cada um desses valores, vamos printar o valor da variável
    print(x)